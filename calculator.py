def add(n1,n2):
    return n1 + n2

def sub(n1,n2):
    return n1 - n2

def mul(n1,n2):
    return n1 * n2

def div(n1,n2):
    return n1 / n2
 
operations = {
    "+" : add,
    "-" : sub,
    "*" : mul,
    "/" : div
}

n1 = float(input("What's the first number: "))
op = input("select an operation (+, -, *, /) : ")
n2 = float(input("What's the next number: "))

if op in operations:
    result = operations[op](n1, n2)
    print(f"{n1} {op} {n2} = {result}")
else:
    print("Invalid operation.")